def main() -> None:
    for locale in ["en", "de", "fr", "ja", "cn", "ko", "tc"]:
        path = LOCALES_DIR / f"{locale}.json"
        data = json.loads(path.read_text(encoding="utf-8"))
        added = 0
        updated = 0
        for key, vals in ENTRIES:
            if locale not in vals:
                raise SystemExit(f"missing {locale} translation for {key}")
            new_val = vals[locale]
            if key in data:
                if data[key] != new_val:
                    updated += 1
                    data[key] = new_val
            else:
                added += 1
                data[key] = new_val
        path.write_text(
            json.dumps(data, ensure_ascii=False, indent=4) + "\n",
            encoding="utf-8",
        )
        print(f"{locale}: +{added} new, {updated} updated, total {len(data)}")


if __name__ == "__main__":
    main()
